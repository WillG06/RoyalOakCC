{/* FUNCTIONALITY FILE */}
{/*  If scrolled down midway on previous page, it scrolls to top of new page */}

import { useEffect } from "react";
import { useLocation } from "react-router-dom";

export default function ScrollToTop() {
  const { pathname } = useLocation();

  useEffect(() => {
    window.scrollTo({
      top: 0,
      behavior: "smooth"
    });
  }, [pathname]);

  return null;
}
